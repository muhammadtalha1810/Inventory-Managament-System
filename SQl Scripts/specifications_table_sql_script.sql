(
	SPECIFICATIONID INT PRIMARY KEY IDENTITY(1,1),
	SCREENSIZE DECIMAL(3,1) NOT NULL,
	BATTERYSIZE INT NOT NULL,
	SIMSLOTS INT NOT NULL,
	CHARGINGSPEED INT NOT NULL,
	MODELID INT,
	FOREIGN KEY (MODELID) REFERENCES MODEL(MODELID)
	ON DELETE CASCADE
	ON UPDATE CASCADE
)


