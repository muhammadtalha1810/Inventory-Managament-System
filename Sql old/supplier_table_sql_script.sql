CREATE PROCEDURE GetAllSupplier
AS
BEGIN
		SELECT * FROM SUPPLIER;
END
GO

CREATE PROCEDURE GetSupplierByID
	@Id INT
AS
BEGIN
		SELECT * FROM SUPPLIER WHERE ID = @Id;
End
Go

CREATE PROCEDURE AddSupplier
	@Sname VARCHAR(50),@PhoneNo VARCHAR(11),@SAddress VARCHAR(200)
AS
BEGIN
		INSERT INTO SUPPLIER(SNAME, PHONENO, SADDRESS)
		VALUES(@Sname, @PhoneNo, @SAddress)
End
Go

CREATE PROCEDURE UpdateSupplier
	@Id INT,@Sname VARCHAR(50),@PhoneNo VARCHAR(11),@SAddress VARCHAR(200)
AS
BEGIN
		UPDATE SUPPLIER
		SET SNAME = @Sname, PHONENO = @PhoneNo, SADDRESS = @SAddress
		WHERE ID = @Id;
End
Go


CREATE PROCEDURE DeleteSupplier
	@Id INT
AS
BEGIN
		DELETE FROM SUPPLIER WHERE ID = @Id
End
Go
