CREATE TABLE SUPPLIER
(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	SNAME VARCHAR(50) NOT NULL,
	PHONENO VARCHAR(11) NOT NULL,
	SADDRESS VARCHAR(200) NOT NULL
)

