SELECT * FROM BRANDS;

SELECT COUNT(*) AS COUNTS FROM BRANDS;

CREATE PROCEDURE GetBrands
AS
BEGIN
		SELECT * FROM BRANDS;
END
GO

CREATE PROCEDURE GetBrandByID
	@Id INT
AS
BEGIN
		SELECT * FROM BRANDS WHERE ID = @Id;
End
Go

CREATE PROCEDURE AddBrand
	@Id INT,@Bname VARCHAR(50)
AS
BEGIN
		INSERT INTO BRANDS (BNAME)
		VALUES(@Bname)
End
Go

CREATE PROCEDURE UpdateBrand
	@Id INT,@Bname varchar(50)
AS
BEGIN
		UPDATE BRANDS
		SET BNAME = @Bname
		WHERE ID = @Id;
End
Go


CREATE PROCEDURE DeleteBrand
	@Id INT
AS
BEGIN
		DELETE FROM BRANDS WHERE ID = @Id
End
Go