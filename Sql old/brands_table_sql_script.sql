USE[Inventory_Management_System]

CREATE TABLE BRANDS
(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	BNAME VARCHAR(50) NOT NULL,
)

INSERT INTO BRANDS(BNAME)
