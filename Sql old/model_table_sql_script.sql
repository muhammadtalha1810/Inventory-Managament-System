USE[Inventory_Management_System]

CREATE TABLE MODEL
(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	MNAME VARCHAR(50) NOT NULL,
	RELEASEDATE DATE NOT NULL,
	BRANDID INT NOT NULL
)

