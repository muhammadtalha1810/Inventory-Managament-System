CREATE PROCEDURE GetAllMODELS
AS
BEGIN
		SELECT * FROM MODEL;
END
GO

CREATE PROCEDURE GetModelByID
	@Id INT
AS
BEGIN
		SELECT * FROM MODEL WHERE ID = @Id;
End
Go

CREATE PROCEDURE AddModel
	@Mname VARCHAR(50),@ReleaseDate DATE,@BrandId INT
AS
BEGIN
		INSERT INTO MODEL(MNAME, RELEASEDATE, BRANDID)
		VALUES(@Mname, @ReleaseDate, @BrandId)
End
Go

CREATE PROCEDURE UpdateModel
	@Id INT,@Mname VARCHAR(50),@ReleaseDate DATE,@BrandId INT
AS
BEGIN
		UPDATE MODEL
		SET MNAME = @Mname, RELEASEDATE = @ReleaseDate, BRANDID = @BrandId
		WHERE ID = @Id;
End
Go


CREATE PROCEDURE DeleteModel
	@Id INT
AS
BEGIN
		DELETE FROM MODEL WHERE ID = @Id
End
Go
